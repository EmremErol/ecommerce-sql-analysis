-- =========================================
-- 1. CUSTOMER TRANSACTION DETAILS
-- =========================================
-- Nazlýcan ÖZSÝMÝTÇÝ isimli kullanýcýnýn yaptýđý iţlemleri tek tabloda görmek

SELECT 
    U.USERNAME_ AS KULLANICIADI,
    U.NAMESURNAME AS ADSOYAD,
    CT.CITY AS IL,
    D.DISTRICT AS SEMT,
    A.ADDRESSTEXT AS ACIKADRES,
    P.DATE_ AS ODEMETARIHI,
    P.APPROVECODE AS BANKAONAYKODU, 
    I.DATE_ AS FATURATARIHI,
    I.CARGOFICHENO AS KARGOFISNO
FROM ORDERS O
INNER JOIN USERS U ON O.USERID = U.ID
INNER JOIN ADDRESS A ON A.ID = O.ADDRESSID
INNER JOIN CITIES CT ON CT.ID = A.CITYID
INNER JOIN DISTRICTS D ON D.ID = A.TOWNID
INNER JOIN PAYMENTS P ON P.ORDERID = O.ID
INNER JOIN INVOICES I ON I.ORDERID = O.ID
WHERE U.NAMESURNAME = 'Nazlýcan ÖZSÝMÝTÇÝ';


-- =========================================
-- 2. ORDER DETAILS WITH PRODUCT INFORMATION
