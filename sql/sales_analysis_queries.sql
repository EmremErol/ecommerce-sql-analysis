-- =========================================
-- 3. CITY-BASED ORDER ANALYSIS
-- =========================================
-- Ţehirlere göre toplam sipariţ tutarý, miktarý ve sipariţ sayýsý

SELECT
    CT.CITY AS SEHIRADI,
    SUM(OD.LINETOTAL) AS TOPLAMSIPARIS_TUTARI,
    SUM(OD.AMOUNT) AS TOPLAMSIPARIS_ADEDI,
    COUNT(OD.ID) AS TOPLAMSIPARIS_SAYISI
FROM ORDERS O
INNER JOIN USERS U ON O.USERID = U.ID
INNER JOIN ADDRESS A ON A.ID = O.ADDRESSID
INNER JOIN CITIES CT ON CT.ID = A.CITYID
INNER JOIN DISTRICTS D ON D.ID = A.TOWNID
INNER JOIN PAYMENTS P ON P.ORDERID = O.ID
INNER JOIN INVOICES I ON I.ORDERID = O.ID
INNER JOIN ORDERDETAILS OD ON OD.ORDERID = O.ID
INNER JOIN ITEMS ITM ON ITM.ID = OD.ITEMID
GROUP BY CT.CITY
ORDER BY COUNT(OD.ID) DESC;


-- =========================================
-- 4. CATEGORY-BASED SALES ANALYSIS
-- =========================================
-- Ürün kategorilerine göre sipariţ dađýlýmý

SELECT 
    ITM.CATEGORY1,
    ITM.CATEGORY2,
    ITM.CATEGORY3,
    SUM(OD.LINETOTAL) AS TOPLAMSIPARIS_TUTARI,
    SUM(OD.AMOUNT) AS TOPLAMSIPARIS_ADEDI,
    COUNT(OD.ID) AS TOPLAMSIPARIS_SAYISI,
    SUM(OD.LINETOTAL) / SUM(OD.AMOUNT) AS ORTALAMABIRIMFIYAT
FROM ORDERS O
INNER JOIN USERS U ON O.USERID = U.ID
INNER JOIN ADDRESS A ON A.ID = O.ADDRESSID
INNER JOIN CITIES CT ON CT.ID = A.CITYID
INNER JOIN DISTRICTS D ON D.ID = A.TOWNID
INNER JOIN PAYMENTS P ON P.ORDERID = O.ID
INNER JOIN INVOICES I ON I.ORDERID = O.ID
INNER JOIN ORDERDETAILS OD ON OD.ORDERID = O.ID
INNER JOIN ITEMS ITM ON ITM.ID = OD.ITEMID
GROUP BY ITM.CATEGORY1, ITM.CATEGORY2, ITM.CATEGORY3
ORDER BY ITM.CATEGORY1;


